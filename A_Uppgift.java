    // Start simulation (all code for simulation is here)
    private void btnStartSimulationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartSimulationActionPerformed
        
        // disable the button and controls while simulation is in progress
        btnStartSimulation.setEnabled(false);
        setPanelEnabled(pnlFirstChoice, false);
        setPanelEnabled(pnlFinalChoice, false);
        setPanelEnabled(pnlSimulationsNumber, false);
        
        // load number of simulations required by  user
        int simulationsNumber = Integer.valueOf(txfSimulationsNumber.getText());
        // Replace 0 with 1
        if (simulationsNumber == 0) {
            simulationsNumber = 1;
            txfSimulationsNumber.setText("1");
        }
        
        // Counter for how many simulations ended with player winning the car
        int playerWonCar = 0;
        
        // Simulation loop
        for (int i = 1; i <= simulationsNumber; i++) {
            
            // Determine car position
            char simulatedCarPosition = getCarPosition();
            
            // Determine first player choice
            char simulatedPlayerChoice;
            {
            if  (rbtnFirstChoiceA.isSelected())
                simulatedPlayerChoice = 'A';
            else if (rbtnFirstChoiceB.isSelected()) 
                simulatedPlayerChoice = 'B';
            else if (rbtnFirstChoiceC.isSelected()) 
                simulatedPlayerChoice = 'C';
            else
                simulatedPlayerChoice = getCarPosition();
            }
      
             // Determine which curtain the host will open
            char simulatedHostOpens = findGoat(simulatedPlayerChoice, simulatedCarPosition);
            
            // Determine if the player will change the choice
            char willPlayerChange;
            {
            if  (rbtnFinalChoiceYes.isSelected()) {
                willPlayerChange = 'Y';
            } else if (rbtnFinalChoiceNo.isSelected()) {
                willPlayerChange = 'N';
            } else {
                java.util.Random rng = new java.util.Random();
                char variants[] = {'Y', 'N'};
                willPlayerChange =  variants[rng.nextInt(2)];
            }}
            
            // Determine final choise
            char simulatedFinalChoice;
            {
            if (willPlayerChange == 'Y') {// players changes choice            
                simulatedFinalChoice = "ABC".replace(String.valueOf(simulatedHostOpens), "").replace(String.valueOf(simulatedPlayerChoice), "").charAt(0);
            } else {// players choice remains the same
                simulatedFinalChoice = simulatedPlayerChoice;        
            }}
            
            // if player won the car increase the counter
            if (simulatedFinalChoice == simulatedCarPosition)
                playerWonCar++;     

        }
        
        // This is fake "simulation process" made with timer delay only for smoother user expirience
        // at the end it adds stats
        
        final int playerWonCarFinal = playerWonCar;
        final int simulationsNumberFinal = simulationsNumber;
        String willPlayerChangeStats;
        {
        if  (rbtnFinalChoiceYes.isSelected()) {
            willPlayerChangeStats = "Ja;       ";
        } else if (rbtnFinalChoiceNo.isSelected()) {
            willPlayerChangeStats = "Nej;     ";
        } else {
            willPlayerChangeStats = "Slump;";
        }}
        String playerChoiceStats;
        {
        if  (rbtnFirstChoiceA.isSelected())
            playerChoiceStats = "A;        ";
        else if (rbtnFirstChoiceB.isSelected()) 
            playerChoiceStats = "B;        ";
        else if (rbtnFirstChoiceC.isSelected()) 
            playerChoiceStats = "C;        ";
        else
            playerChoiceStats = "Slump;";
        }
        
        javax.swing.Timer timer = new javax.swing.Timer(1, new java.awt.event.ActionListener() {
            int i = 1;
            int totalIterations = 40;
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e) {
                    if (i <= totalIterations) {
                        // Update progress bar
                        int percentComplete = (i * 100) / totalIterations;
                        pgbProgress.setValue(percentComplete);                     
                        i++;
                    } else {
                        
                        ((javax.swing.Timer) e.getSource()).stop();  // Stop the timer after all iterations
                        
                        // Add simulation stats
                        javax.swing.text.StyledDocument doc = txpSimulationResults.getStyledDocument();
                        try {
                            javax.swing.text.SimpleAttributeSet bold = new javax.swing.text.SimpleAttributeSet();
                            javax.swing.text.StyleConstants.setBold(bold, true);
                            
                            doc.insertString(doc.getLength(), "Gardin: ", null);
                            doc.insertString(doc.getLength(), playerChoiceStats, bold);
                            doc.insertString(doc.getLength(), " Ändrade: ", null);
                            doc.insertString(doc.getLength(), willPlayerChangeStats, bold);
                            doc.insertString(doc.getLength(), " Bil: " + playerWonCarFinal + "/" + simulationsNumberFinal + "   ", null);
                            doc.insertString(doc.getLength(), String.format("%.2f", (playerWonCarFinal * 100.0 / simulationsNumberFinal)) + "%\n", bold);
                            
                        } catch (javax.swing.text.BadLocationException ex) {
                            ex.printStackTrace();
                        }
                        
                        // enable the button and controls
                        btnStartSimulation.setEnabled(true);  
                        setPanelEnabled(pnlFirstChoice, true);
                        setPanelEnabled(pnlFinalChoice, true);
                        setPanelEnabled(pnlSimulationsNumber, true);
                    }
            }
        });
        timer.start(); 
    }//GEN-LAST:event_btnStartSimulationActionPerformed

