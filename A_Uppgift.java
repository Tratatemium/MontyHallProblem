    // This is filter for txfSimulationsNumber text field.
    // The purpoise is to let user fill in only digits and only up to maximum amount     
    class NumericDocumentFilter extends javax.swing.text.DocumentFilter {
        
        private int maxLength;

        public NumericDocumentFilter(int maxLength) {
            this.maxLength = maxLength;
        }        

        @Override
        public void insertString(FilterBypass fb, int offset, String string, javax.swing.text.AttributeSet attr) throws javax.swing.text.BadLocationException {
            if ((fb.getDocument().getLength() + string.length()) <= maxLength && string.matches("\\d+")) {
                super.insertString(fb, offset, string, attr);
            }
        }

        @Override
        public void replace(FilterBypass fb, int offset, int length, String text, javax.swing.text.AttributeSet attrs) throws javax.swing.text.BadLocationException {
            if ((fb.getDocument().getLength() - length + text.length()) <= maxLength && text.matches("\\d+")) {
                super.replace(fb, offset, length, text, attrs);
            }
        }
    }
    

