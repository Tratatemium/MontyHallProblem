    // Function that finds a goat to be revealed by the host after mlayer made first choice
    private char findGoat(char playerChoice, char carPosition) {
        char goatPosition;
        if (playerChoice != carPosition) { 
            // if player have chosen a goat
            // return curtain with the other goat
            goatPosition = "ABC".replace(String.valueOf(carPosition), "").replace(String.valueOf(playerChoice), "").charAt(0);
        } else { 
            // if player have chosen the car
            // choose one of remaining goats at random
            java.util.Random rng = new java.util.Random();
            goatPosition = "ABC".replace(String.valueOf(playerChoice), "").charAt(rng.nextInt(2));
        }
        return goatPosition;
    }
    

    /** Function that is activated after player has chosen the curtain:
    *   The host announces player choice
    *   He opens curtain with the goat
    *   He suggests a change of choice
    *   Buttons Yes / No appear
    *   Function returnes which curtain (with goat) the host has opened
    */  
    private char firstPlayerChoice(char playerChoice) {
        // Indicate player choice by showing an arrow
        showChoiceArrow(playerChoice);      
        
        // find cutrain to reveal a goat
        char curtainToOpen = findGoat(playerChoice, carPosition);
       
        // the host declares player choice and that he is goeing to reveal a goat
        txpHostText.setText(
                "Spelaren har valt gardin " + playerChoice + 
                "! Nu kommer jag att avslöja att det inte finns vinsten bakom gardinen " + curtainToOpen +
                "!"
        );
        
        // reveal the goat
        curtainReveal(curtainToOpen);       
       
        // after a pause the host questions if the player wnts to change his choice, buttons yes/no appear
        javax.swing.Timer timer2 = new javax.swing.Timer(3000, new java.awt.event.ActionListener() {
        @Override
        public void actionPerformed(java.awt.event.ActionEvent e) {
            
                char curtainLeft = "ABC".replace(String.valueOf(curtainToOpen), "").replace(String.valueOf(playerChoice), "").charAt(0);
                
                txpHostText.setText(
                "<html>Vill du byta ditt val från gardin " + playerChoice + 
                " till gardin " + curtainLeft + "?"
                );
                
                javax.swing.SwingUtilities.invokeLater(() -> { // make sure update of button visibility happens after timer finishes
                    btnYes.setVisible(true);
                    btnYes.setFocusable(true);
                    btnYes.revalidate();
                    btnYes.repaint();

                    btnNo.setVisible(true);
                    btnNo.setFocusable(true);
                    btnNo.revalidate();
                    btnNo.repaint();

                    pnlGame.revalidate();
                    pnlGame.repaint();
                });
            }
        });
        timer2.setRepeats(false);
        timer2.start(); // Start the timer 
        
        return curtainToOpen;
    }
    
    
    /** Function that is activated after player has chosen if he is sure:
    *   All the curtains open
    *   The host announses what the player won
    *   Results of the game are added to stats field
    *   Button Play Again is enabled
    */ 
    private void secondPlayerChoice(String choice) {
        
        if (choice.equals("Yes")) {// players changes choice
            
            finalChoice = "ABC".replace(String.valueOf(hostOpened), "").replace(String.valueOf(playerChoice), "").charAt(0);
        
            btnYes.setBorder(javax.swing.BorderFactory.createCompoundBorder( //hihglite player choice
                javax.swing.BorderFactory.createLineBorder(new Color(255, 215, 0), 4),
                javax.swing.BorderFactory.createEmptyBorder(5, 5, 5, 5)
            ));
            
        } else if (choice.equals("No")) {// players choice remains the same
            
            finalChoice = playerChoice;
            btnNo.setBorder(javax.swing.BorderFactory.createCompoundBorder( //hihglite player choice
                javax.swing.BorderFactory.createLineBorder(new Color(255, 215, 0), 4),
                javax.swing.BorderFactory.createEmptyBorder(5, 5, 5, 5)
            ));           
        }        
        
        // update player choice pointer
        showChoiceArrow(finalChoice);        
        
        // reveal what player has won
        curtainReveal(finalChoice);        
        curtainReveal("ABC".replace(String.valueOf(hostOpened), "").replace(String.valueOf(finalChoice), "").charAt(0)); 
        
        // disable buttons
        btnYes.setEnabled(false);
        btnNo.setEnabled(false);
        
        String playerPrize = finalChoice == carPosition ? "bil" : "get";
        
        javax.swing.Timer timer = new javax.swing.Timer(1000, new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e) {
                // append result of this round
                txaResults.append(
                        "Första valet: " + playerChoice +
                        ", ändrade valet: " + (choice.equals("Yes") ? "Ja " : "Nej") +
                        ", vinst: en " + playerPrize + "\n"
                );                
            }
        });
        timer.setRepeats(false);
        timer.start(); // start the timer
        
        // announce what the player has won (in bold)
        javax.swing.text.StyledDocument doc = txpHostText.getStyledDocument();
        try {
            javax.swing.text.SimpleAttributeSet bold = new javax.swing.text.SimpleAttributeSet();
            javax.swing.text.StyleConstants.setBold(bold, true);
            doc.insertString(doc.getLength(), "\nSpelaren har vunnit en " + playerPrize + "!", bold);
        } catch (javax.swing.text.BadLocationException e) {
            e.printStackTrace();
        }
        
        //update of button visibility
        javax.swing.SwingUtilities.invokeLater(() -> { 
            btnYes.setVisible(true);
            btnYes.setFocusable(true);
            btnYes.revalidate();
            btnYes.repaint();

            btnNo.setVisible(true);
            btnNo.setFocusable(true);
            btnNo.revalidate();
            btnNo.repaint();

            pnlGame.revalidate();
            pnlGame.repaint();
        });
        
        // Now game can be reset
        btnPlayAgain.setEnabled(true);
        
    }
    
